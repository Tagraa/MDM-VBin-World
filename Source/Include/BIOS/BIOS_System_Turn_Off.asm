;***********************************************************************
; System Power Off
;***********************************************************************
System.Turn.Off:
        mov     byte [Error], 'A'
        call    BIOS_15h.Check.APM.Service.Is.Present
        jc      .Err
        inc     byte [Error]
        cmp     bx, 504dh
        jne     .Err
;.......................................................................
        inc     byte [Error]
        call    BIOS_15h.Connect.RM.Interface
        jc      .Err
;.......................................................................
        inc     byte [Error]
        call    BIOS_15h.Switch.To.APM           ; APM 1.1+
        jc      .Err
        inc     byte [Error]
        cmp     al, 01h
        jb      .Err
;.......................................................................
        inc     byte [Error]
        call    BIOS_15h.Enable.APM
        jc      .Err
;.......................................................................
        inc     byte [Error]
        call    BIOS_15h.Engage.APM
        jc      .Err
;.......................................................................
        inc     byte [Error]
        call    BIOS_15h.Turn.Off.System
        jc      .Err
;.......................................................................
        jmp     .End
;.......................................................................
    .Err:
        xor     di, di
        mov     ah, 09h
        mov     al, byte [Error]
        stosw
;.......................................................................
    .End:
        cli
        hlt
;.......................................................................
Error:
    db  0
;.......................................................................
        ret
